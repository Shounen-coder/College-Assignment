// Java code to check whether a character is a Vowel or Not

public class VowelOrNot {
  public static void main(String[] args) {
    char ch = 'i';
    if(ch == 'a' || ch == 'e' || ch == 'i' || ch == 'o' || ch == 'u' )
      System.out.println(ch + " is  a Vowel");
    else
      System.out.println(ch + " is  a Vowel");
  }
}
